def RoundRobin (Procesos, q):
  Cola        = [{"nombre" :  P["nombre"], "restante" : P["burst"]} for P in Procesos] #arreglo con nombres y tiempos de los procesos
  Tiempo = 0
  Espera = {P["nombre"] : 0 for P in Procesos} #tiempo que llevan esperando los procesos en la cola

  while Cola:
    #sacamos el primer elemento disponible
    actual = Cola.pop(0)
    #sacamos estadísticas de tiempo
    turno = min(q,actual ["restante"]) #se escoge al valor más pequeño entre q y restante
    sobra = actual ["restante"] - turno
    print(f"""En el tiempo {Tiempo}, el proceso {actual ["nombre"]} correra por {turno} ms, y le falta {sobra} ms para terminar""")

    for esperando in Cola:
      Espera[esperando ["nombre"]] = Espera[esperando ["nombre"]] + turno #se suma a Espera el turno del proceso actual

    actual["restante"] = sobra #restante adquiere el valor sobrante del tiempo de vida
    Tiempo = Tiempo + turno

    if actual ["restante"] > 0: #si el tiempo de vida restante es mayor a cero, el proceso regresa al final de la cola
      Cola.append(actual)
    else:
      print(f"El proceso {actual ["nombre"]} TERMINÓ")

  promedio = sum(Espera.values()) / len(Espera)
  print(f"El tiempo promedio de espera de los procesos es de {promedio} ms")


