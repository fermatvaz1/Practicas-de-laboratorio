import multiprocessing
import time
import random
#el proceso genera nums aleatorios
def productor(memoria,semaforo):
  for i in range(5): #genera 5 nums
    valor = random.randint(1,1000)
    semaforo.acquire() #es un while/espera*tarea


    #quiero leer la memoria, me interesa que otro proc no lo esté usando
    memoria[i] = valor
    semaforo.release() #signal
    time.sleep(0.15)
    #time.sleep (0.60) -> aumentamos tiempo de bloqueo en productor

#el proceso se mueve y lee
def consumidor(memoria,semaforo):
  for i in range (5): #lee 5 nums
    time.sleep(0.2)
    #time.sleep (0.60) -> aumentamos tiempo de bloqueo en consumidor
    semaforo.acquire()
    print(f"Consumidor lee: {memoria[i]} en {i}")
    semaforo.release()


##simulación de semáforo y memoria con librerías importadas
memoria = multiprocessing.Array("i", [0]*5)
#semaforo = multiprocessing.Semaphore(1)
#semaforo = multiprocessing.Semaphore(0)
semaforo = multiprocessing.Semaphore(2)
#0 Los recursos están bloqueados a otros procesos
#1 Es un mutex, los recursos están disponibles a un proceso
#2 mutex, coordinados


##Se crean 2 procesos
ProcProductor = multiprocessing.Process(target = productor,
                                        args=(memoria,semaforo))
ProcConsumidor = multiprocessing.Process(target = consumidor,
                                        args=(memoria,semaforo))


#una vez que un proceso termina sus tareas hay que matarlo
#pareciera que se lanzan a diferentes momentos, pero los proc son voraeces, buscaran la forma de volver a trabajar, yendo al scheduling
ProcProductor.start()
ProcConsumidor.start()
##se junta con el proceso padre
ProcProductor.join()
ProcConsumidor.join()